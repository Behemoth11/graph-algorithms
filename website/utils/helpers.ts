
export function sleep(ms: number) : Promise<void> {
    return new Promise((r) => setTimeout(r, ms));
}

export function getRandomColor() {
  var letters = '0123456789ABCDEF';
  var color = '#';
  for (var i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * 16)];
  }
  return color;
}